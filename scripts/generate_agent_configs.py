def generate_all(manifest: dict, check: bool = False, target: str | None = None) -> bool:
    validate_manifest(manifest)
    roles = manifest["roles"]
    profiles = manifest["tool_profiles"]
    ok = True

    targets = {"agents_md", "codex", "claude", "skills", "docs"} if target is None else {target}

    # AGENTS.md
    if "agents_md" in targets:
        path = ROOT / "AGENTS.md"
        content = render_agents_md(manifest)
        if not write_or_check(path, content, check):
            ok = False

    # Codex config
    if "codex" in targets:
        path = ROOT / ".codex" / "config.toml"
        content = render_codex_config(manifest)
        if not write_or_check(path, content, check):
            ok = False

        for name, role in roles.items():
            path = ROOT / ".codex" / "agents" / f"{name}.toml"
            content = render_codex_agent(name, role, manifest)
            if not write_or_check(path, content, check):
                ok = False

    # Skills
    if "skills" in targets:
        for name, role in roles.items():
            path = ROOT / ".agents" / "skills" / name / "SKILL.md"
            content = render_skill(name, role, manifest)
            if not write_or_check(path, content, check):
                ok = False

    # Claude Code
    if "claude" in targets:
        for name, role in roles.items():
            path = ROOT / ".claude" / "agents" / f"{name}.md"
            content = render_claude_agent(name, role, manifest)
            if not write_or_check(path, content, check):
                ok = False

    # Docs
    if "docs" in targets:
        docs = render_docs(manifest)
        for rel_path, content in docs.items():
            path = ROOT / rel_path
            if not write_or_check(path, content, check):
                ok = False

    return ok


