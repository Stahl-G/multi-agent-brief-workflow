def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="generate_agent_configs",
        description="Generate platform-specific agent configs from agent_roles.yaml",
    )
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument("--write", action="store_true", help="Generate all files")
    group.add_argument("--check", action="store_true", help="Check if generated files are up to date")
    parser.add_argument("--target", choices=["codex", "claude", "skills", "docs", "agents_md"],
                        help="Generate only a specific target")
    parser.add_argument("--manifest", type=Path, default=MANIFEST_PATH,
                        help="Path to agent_roles.yaml")

    args = parser.parse_args(argv)

    manifest = load_manifest(args.manifest)
    ok = generate_all(manifest, check=args.check, target=args.target)

    if args.check:
        if ok:
            print("All generated files are up to date.")
        else:
            print("Generated files are stale. Run: python scripts/generate_agent_configs.py --write")
        return 0 if ok else 1
    else:
        print("Generated agent configs.")
        return 0


